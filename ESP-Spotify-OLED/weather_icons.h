const unsigned char* getWeatherIcon(const char* icon_code) {
  // https://openweathermap.org/weather-conditions#Weather-Condition-Codes-2
  if (strcmp(icon_code, "01d") == 0) return wi_day_clear;                                    // Clear day
  if (strcmp(icon_code, "01n") == 0) return wi_night_clear;                                  // Clear night
  if (strcmp(icon_code, "02d") == 0) return wi_day_cloud;                                    // Few clouds day
  if (strcmp(icon_code, "02n") == 0) return wi_night_cloud;                                  // Fey clouds night
  if (strcmp(icon_code, "03d") == 0 || strcmp(icon_code, "03n") == 0) return wi_cloud;       // Clouds
  if (strcmp(icon_code, "04d") == 0 || strcmp(icon_code, "04n") == 0) return wi_very_cloudy; // Broken clouds
  if (strcmp(icon_code, "09d") == 0 || strcmp(icon_code, "09n") == 0) return wi_showers;     // Few rain
  if (strcmp(icon_code, "10d") == 0 || strcmp(icon_code, "10n") == 0) return wi_rain;        // Rain
  if (strcmp(icon_code, "11d") == 0 || strcmp(icon_code, "11n") == 0) return wi_storm;       // Thunderstorm
  if (strcmp(icon_code, "13d") == 0 || strcmp(icon_code, "13n") == 0) return wi_snow;        // Snow
  if (strcmp(icon_code, "50d") == 0 || strcmp(icon_code, "50n") == 0) return wi_fog;         // Fog

  return wi_error;  // Return default error icon
}

